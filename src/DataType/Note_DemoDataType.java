package DataType;

public class Note_DemoDataType {
    public static void main(String[] args) {

        System.out.println("String: \"Selenium\"");
        System.out.println("int: 1, 123, 2343");
        System.out.println("long: 1,231,432,424L");
        System.out.println("char: 'a', 'b', '1', '2' ");
        System.out.println("float: 12.5F$, 1.23245F (decimal)");
        System.out.println("boolean: true, false");
        System.out.println("Use most often: String, int, boolean, double");

        float decimal = 1.231F;
        System.out.println(decimal+"$");

    }
}

package Array;

public class DemoArray_2 {
    public static void main(String[] args) {

        int mangNumber[] = {1, 34, 6, 62, 8, 21};

        for (int i=0; i < mangNumber.length; i++){
            System.out.println(mangNumber[i]);
        }

        System.out.println("Improve");
        for (int number : mangNumber){
            System.out.println(number);
        }


    }
}
